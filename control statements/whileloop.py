'''

CONTINUE STATEMENT 

With the continue statement we can stop the current iteration, and continue with the next:


i = 0;      #INITIALIZATION
while i < 6:        #CONDITION CHECK
  i += 1        #INCREAMENT i=1
  if i == 3:        #CONDITION 
    continue        #EXECUTABLE BLOCK
  print(i)      #STATEMENT
'''




